# 2.4.2 Floats

# Addition
print("Addition")
addition = 1.0+1.0
print(f"1.0+1.0={addition}\n")

# Subtraction
print("Subtraction")
subtraction = 28.0-1.0
print(f"28.0-1.0={subtraction}\n")

# Multiplication
print("Multiplication")
multiplication = 25.0*4.0
print(f"25.0*4.0={multiplication}\n")

# Division
print("Division")
division = 100.0/4.0
print(f"100.0/4.0={division}\n")

# Exponentiation 
print("Exponentiation")
exponentiation = 3.0**3.0
print(f"3.0**3.0={exponentiation}\n")

# Order of Operations
print("Order of Operations")
oop = 2.0-7.0+3.0*8.0+1.0
print(f"2.0-7.0+3.0*8.0+1.0={oop}\n")

# Spacing
print("Spacing")
space1 = 2.0  -7.0+ 3.0*8.0+  1.0
print(f" 2  -7+ 3*8+  1={space1}")
space2 = 2.0-7.0+3.0*8.0+1.0
print(f"2-7+3*8+1={space2}\n")