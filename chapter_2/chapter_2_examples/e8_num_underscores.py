# 2.4.4 Underscores in numbers
# You can add underscores to numbers for readibility but
# they'll still print without the underscores.

num_underscores = 1_000_000
print(f"num_underscores stored as 1_000_000 but when printed = {num_underscores}")

num = 1000000
print(f"num stored as 1000000 and when printed = {num}")